#include "header.h"

int	ft_check_nbrfl(char *str, int i)
{
	i = 0;
	while (str[i] && i != 42)
	{
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else
			i = 42;
		if (i == ft_strlen(str))
			return (1);
	}
	return (0);
}
