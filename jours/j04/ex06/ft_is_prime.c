int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 2)
		return (0);
	else if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	while (i < nb && i < 100000000)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
